
/**
 * The Enum Colour represents the possible colours of the LEDs.
 */
public enum Colour 
{
	
	/** The colour white. */
	WHITE,
	
	/** The colour red. */
	RED,
	
	/** The colour green. */
	GREEN,
	
	/** The colour blue. */
	BLUE,
	
	/** The colour yellow. */
	YELLOW,
	
	/** The colour magenta. */
	MAGENTA,
	
	/** The colour cyan. */
	CYAN
}
