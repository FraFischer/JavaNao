
/**
 *  Possible Axle for the moving
 */
public enum Axle 
{
	
	/** Axle pitch. */
	PITCH,
	
	/** Axle yaw. */
	YAW,
	
	/** Axle roll. */
	ROLL,
	
	/** Axle yawpitch. */
	YAWPITCH,
	
	/** Axle open, just possible by the joint hand. */
	OPEN,
	
	/** Axle close, just possible by the joint hand.*/
	CLOSE
}
